    private void btn_pro_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pro_saveActionPerformed
        // Save Product details
        String prod_Name=txt_pro_name.getText();
        String prod_BNo=txt_pro_barNo.getText();
        double prod_price=Double.parseDouble(txt_pro_price.getText());
        int prod_qty=Integer.parseInt(txt_pro_qty.getText());
        int sup_id=Integer.parseInt(txt_pro_supID.getText());
        int reQty=Integer.parseInt(txt_re_qun.getText());
        
        int result;
         try {
            Statement stmt=db.myCon().createStatement();
            String sql= "INSERT INTO product(prod_Name,bar_No,price,Qty,sup_ID,reOrder) VALUES ('"+prod_Name+"','"+prod_BNo+"','"+prod_price+"','"+prod_qty+"','"+sup_id+"','"+reQty+"')";
            result = stmt.executeUpdate(sql);
             if (result==1) {
                 JOptionPane.showMessageDialog(null, "Data stored successfully");
             }
             else{
                JOptionPane.showMessageDialog(null, "Data storage failed");
            }
        } catch (Exception e) {
             System.out.println(e);
        }
         tb_load();
    }//GEN-LAST:event_btn_pro_saveActionPerformed

