    private void btn_pro_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pro_searchActionPerformed
        // search product details
        String prod_id=txt_pro_id.getText();
        
        try {
            Statement stmt = db.myCon().createStatement();
            String sql="SELECT * FROM product WHERE (prod_ID = '"+prod_id+"') OR (bar_No='"+prod_id+"')";
            ResultSet rs = stmt.executeQuery(sql);
             if (rs.next()) {
                txt_pro_name.setText(rs.getString("prod_Name"));
                txt_pro_barNo.setText(rs.getString("bar_No"));
                txt_pro_price.setText(rs.getString("price"));
                txt_pro_qty.setText(rs.getString("Qty"));
                txt_pro_supID.setText(rs.getString("sup_ID"));
                txt_re_qun.setText(rs.getString("reOrder"));
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        tb_load();
    }//GEN-LAST:event_btn_pro_searchActionPerformed

